// classifier.js — تصنيف المصاريف تلقائيًا بالاعتماد على كلمات مفتاحية (نموذج أولي قابل للتطوير إلى تعلم آلي لاحقًا)

const CATEGORY_KEYWORDS = {
  'مطاعم وكافيهات': ['مطعم', 'مطاعم', 'كافيه', 'قهوة', 'ستاربكس', 'برجر', 'وجبة', 'مقهى', 'كافية', 'دجاج', 'بيتزا'],
  'مواصلات': ['بنزين', 'وقود', 'اوبر', 'أوبر', 'كريم', 'تاكسي', 'مواصلات', 'صيانة سيارة', 'باركنج', 'موقف'],
  'فواتير وخدمات': ['فاتورة', 'كهرباء', 'مياه', 'ماء', 'اتصالات', 'stc', 'موبايلي', 'زين', 'انترنت', 'جوال', 'اشتراك شهري'],
  'تسوق': ['سوق', 'تسوق', 'ملابس', 'امازون', 'أمازون', 'نون', 'سلة', 'مول', 'ماركت'],
  'صحة': ['صيدلية', 'دواء', 'مستشفى', 'عيادة', 'تأمين طبي', 'طبيب'],
  'ترفيه': ['سينما', 'العاب', 'ألعاب', 'نتفلكس', 'شاهد', 'ترفيه', 'رحلة', 'سفر'],
  'تعليم': ['دورة', 'كتاب', 'جامعة', 'مدرسة', 'كورس', 'تدريب'],
  'إيجار وسكن': ['إيجار', 'ايجار', 'سكن', 'صك', 'عقار'],
};

function classifyExpense(description = '') {
  const text = description.toLowerCase().trim();
  if (!text) return 'أخرى';

  for (const [category, keywords] of Object.entries(CATEGORY_KEYWORDS)) {
    for (const kw of keywords) {
      if (text.includes(kw.toLowerCase())) {
        return category;
      }
    }
  }
  return 'أخرى';
}

module.exports = { classifyExpense, CATEGORY_KEYWORDS };
